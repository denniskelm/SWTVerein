package client;


/**
 * @author Dennis Kelm
 * Speichert die Unicode Umlaute bequem
 */
public class Umlaut {


    public static String ue() {
        return "\u00FC";
    }

    public static String oe() {
        return "\u00F6";
    }

    public static String ae() {
        return "\u00E4";
    }

    public static String Ue() {
        return "\u00DC";
    }

    public static String Oe() {
        return "\u00D6";
    }

    public static String Ae() {
        return "\u00C4";
    }


}
